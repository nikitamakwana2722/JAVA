class fun 
{
	
	  	static void ad()
			 {
			     System.out.println("This is Addition Function");
			 }
		static void ad(int a)
		{
			System.out.println("NEXT Function =>"+a);
		}
		public static void main(String s[])
		{
			//OVERLOADING
           ad();
           ad(2);
        }		   
}			    